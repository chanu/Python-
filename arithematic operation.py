import numpy as np
scalar1=np.array(2)
scalar2=np.array(4)
add_result_0d = scalar1+scalar2
subtract_result_0d = scalar1-scalar2
multiply_result_0d = scalar1*scalar2
divide_result_0d = scalar1/scalar2

print("0D Add:",add_result_0d)
print("0D Subtract:",subtract_result_0d)
print("0D Multiply:",multiply_result_0d)
print("0D Divide:",divide_result_0d)

#1-Dimensional Arithmetic
array1d=np.array([2,4,5,7,8])
add_result_1d = array1d+10
subtract_result_1d = array1d-2
multiply_result_1d = array1d*3
divide_result_1d = array1d/2

print("1D Array:",array1d)
print("1D Add:",add_result_1d)
print("1D Subtract:",subtract_result_1d)
print("1D Multiply:",multiply_result_1d)
print("1D Divide:",divide_result_1d)

